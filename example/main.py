GlobalParams = collections.namedtuple('GlobalParams', [
    'batch_norm_momentum', 'batch_norm_epsilon', 'dropout_rate',
    'width_coefficient', 'depth_coefficient','depth_divisor', 
    'min_depth', 'drop_connect_rate', 'image_size',
    'num_classes'])

